import fs from 'node:fs';
import path from 'node:path';
import matter from 'gray-matter';
import { marked } from 'marked';

const rootDir = process.cwd();
const blogDir = path.join(rootDir, 'src/content/blog');
const changelogPath = path.join(rootDir, 'CHANGELOG.md');
const outputPath = path.join(rootDir, 'src/lib/generated/static-content.ts');

function parseFaqRaw(raw) {
  const trimmed = raw.trim();
  if (!trimmed) return [];

  const items = [];
  const sections = trimmed.split(/^## /m).filter(Boolean);

  for (const section of sections) {
    const lines = section.split('\n');
    const question = lines[0].trim();
    const answer = lines.slice(1).join('\n').trim();
    if (question && answer) {
      items.push({ question, answer });
    }
  }

  return items;
}

function parseBlogPost(slug) {
  const dirPath = path.join(blogDir, slug);
  const indexPath = path.join(dirPath, 'index.md');
  const faqPath = path.join(dirPath, 'faq.md');

  const raw = fs.readFileSync(indexPath, 'utf8');
  const { data, content } = matter(raw);
  const date =
    typeof data.date === 'string'
      ? data.date
      : new Date(data.date).toISOString().slice(0, 10);
  const lastUpdated = data.lastUpdated
    ? typeof data.lastUpdated === 'string'
      ? data.lastUpdated
      : new Date(data.lastUpdated).toISOString().slice(0, 10)
    : date;

  return {
    slug,
    title: data.title,
    subtitle: data.subtitle ?? '',
    excerpt: data.excerpt ?? '',
    tag: data.tag ?? '',
    date,
    lastUpdated,
    readingTime: data.readingTime ?? '',
    body: marked.parse(content, { async: false }),
    faq: fs.existsSync(faqPath)
      ? parseFaqRaw(fs.readFileSync(faqPath, 'utf8'))
      : [],
  };
}

function stripUnreleasedSection(raw) {
  const match = raw.match(/^## Unreleased\n[\s\S]*?(?=^## |\Z)/m);
  if (!match) return raw;

  const withoutSection = raw.replace(match[0], '').replace(/\n{3,}/g, '\n\n').trim();
  return `${withoutSection}\n`;
}

function buildChangelog() {
  const raw = fs.readFileSync(changelogPath, 'utf8');
  const publishedOnly = stripUnreleasedSection(raw);
  const match = publishedOnly.match(/^## ([^\n]+)/m);
  const dateMatch = match?.[1].match(/^(\d{4}-\d{2}-\d{2})/);

  return {
    body: marked.parse(publishedOnly, { async: false }),
    latestLabel: match?.[1] ?? 'No published updates yet',
    lastUpdated: dateMatch?.[1] ?? new Date().toISOString().slice(0, 10),
  };
}

const slugs = fs
  .readdirSync(blogDir, { withFileTypes: true })
  .filter((entry) => entry.isDirectory())
  .map((entry) => entry.name)
  .filter((slug) => fs.existsSync(path.join(blogDir, slug, 'index.md')));

const blogPosts = slugs.map(parseBlogPost).sort((a, b) => b.date.localeCompare(a.date));
const changelog = buildChangelog();

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(
  outputPath,
  [
    '// This file is generated by scripts/generate-static-content.mjs.',
    '// Do not edit it directly; edit src/content/blog or CHANGELOG.md instead.',
    '',
    `export const BLOG_POSTS = ${JSON.stringify(blogPosts, null, 2)};`,
    '',
    `export const CHANGELOG_DATA = ${JSON.stringify(changelog, null, 2)};`,
    '',
  ].join('\n'),
  'utf8'
);

console.log(`Generated ${path.relative(rootDir, outputPath)} (${blogPosts.length} posts)`);
